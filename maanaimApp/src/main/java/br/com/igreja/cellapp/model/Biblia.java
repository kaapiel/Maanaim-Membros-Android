package br.com.igreja.cellapp.model;

public class Biblia {

	private Integer id;
	private String livro;
	private Integer capitulo;
	private Integer nVersiculo;
	private String versiculo;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getLivro() {
		return livro;
	}
	public void setLivro(String livro) {
		this.livro = livro;
	}
	public Integer getCapitulo() {
		return capitulo;
	}
	public void setCapitulo(Integer capitulo) {
		this.capitulo = capitulo;
	}
	public Integer getnVersiculo() {
		return nVersiculo;
	}
	public void setnVersiculo(Integer nVersiculo) {
		this.nVersiculo = nVersiculo;
	}
	public String getVersiculo() {
		return versiculo;
	}
	public void setVersiculo(String versiculo) {
		this.versiculo = versiculo;
	}
	
	

}
